# 附录A：高阶函数递归小结
# =============================================================================
#
#   cascade(n) —— 直接用 if/else 写死的单函数递归
#       ↓ 抽象
#   f_then_g(f, g, n) —— 把"先做什么、再做什么"参数化
#       ↓ 实例化
#   grow   = f_then_g(grow, print, n) —— 先递归再打印 → 从小到大
#   shrink = f_then_g(print, shrink, n) —— 先打印再递归 → 从大到小
#       ↓ 组合
#   inverse(n) = grow + print(n) + shrink —— 先升后降，形成对称输出
#
# 关键洞察：grow 和 shrink 的 f、g 之一是"递归自身"，另一个是"真正要做的操作"。
# 调换顺序就能翻转方向 —— 这就是"以函数为参数"复用递归结构的威力。


# =============================================================================
# 附录B：lambda 表达式详解
# =============================================================================
#
# 1. 本质
#    lambda 就是不用 def 的"一次性函数"，语法：
#        lambda 参数1, 参数2, ... : 返回值表达式
#
# 2. 对比 def
#    def grow(n):                         grow = lambda n: f_then_g(
#        return f_then_g(grow, print,         grow, print, n // 10
#            n // 10)                     )
#    效果完全等价。lambda 更紧凑，适合函数体只有一个表达式的情况。
#
# 3. 结构拆解（以 grow 为例）
#
#        grow = lambda n: f_then_g(grow, print, n // 10)
#                ─┬─     ───────────────────┬───────────────────
#              关键字+参数              返回值表达式
#
#    grow(12345) 的执行：
#     ① n=12345, n//10=1234
#     ② 调用 f_then_g(grow, print, 1234)
#     ③ 1234 非0 → grow(1234) 再 print(1234)
#     ④ 递归展开，最终输出 1, 12, 123, 1234
#
# 4. 什么时候用？
#    ✓ 函数体只有一行表达式
#    ✓ 传给高阶函数（map、filter、sorted 的 key 等）
#    ✗ 逻辑复杂、需要多行语句 → 用 def
#
# 5. 经典陷阱：延迟绑定
#    funcs = [lambda: i for i in range(3)]
#    print([f() for f in funcs])  # [2, 2, 2]  ← 都是最后一个 i！
#    原因：lambda 里的 i 是"调用时才去查"，不是"定义时就绑定"。
#    修复：funcs = [lambda i=i: i for i in range(3)]  # [0, 1, 2] ✓
