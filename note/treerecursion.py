# 一、单函数递归 — 级联打印
# =============================================================================
# 核心模式：在递归调用的"前"和"后"各放一个操作，就能形成"从上到下再回来"的对称输出。
# 画成调用栈：
#   cascade(12345)
#     print(12345)         ← 下行
#     cascade(1234)
#       print(1234)
#       cascade(123)
#         print(123)
#         cascade(12)
#           print(12)
#           cascade(1)
#             print(1)     ← 谷底
#           print(12)      ← 上行
#         print(123)
#       print(1234)
#     print(12345)
# 输出：12345  1234  123  12  1  12  123  1234  12345

def cascade(n):
    if n < 10:
        print(n)
    else:
        print(n)          # 下行时打印
        cascade(n // 10)  # 递归深入
        print(n)          # 上行时打印

cascade(12345)


# =============================================================================
# 二、高阶函数递归 — 用 f_then_g 抽象递归骨架
# =============================================================================
# 把 cascade 的模式抽出来："先做 f，再做 g" 或 "f 和 g 里有一个是递归自身"。
# 通过调换 f/g 的角色，就能翻转输出方向。

def f_then_g(f, g, n):
    """组合器：若 n 非0,先执行 f(n)，再执行 g(n)。n=0 时停止递归。"""
    if n:
        f(n)
        g(n)

# grow：先递归再打印 → 自底向上，小→大
# 例：grow(12345) → 1  12  123  1234
grow = lambda n: f_then_g(grow, print, n // 10)

# shrink：先打印再递归 → 自顶向下，大→小
# 例：shrink(12345) → 1234  123  12  1
shrink = lambda n: f_then_g(print, shrink, n // 10)

def inverse(n):
    """组合 grow + 顶点 + shrink,形成先升后降的'山峰'输出"""
    grow(n)      # 1  12  123  1234
    print(n)     # 12345（顶点）
    shrink(n)    # 1234  123  12  1

# inverse(12345)
# → 1  12  123  1234  12345  1234  123  12  1


# =============================================================================
# 三、树状递归 — 斐波那契数列
# =============================================================================
# 当递归函数体内调用自身多于一次时，调用图形成"树"（tree recursion）。
# 例如 fib(5):
#                   fib(5)
#                  /      \
#            fib(4)        fib(3)
#           /    \        /    \
#       fib(3) fib(2)  fib(2) fib(1)
#       /    \
#   fib(2) fib(1)
#   /    \
# fib(1) fib(0)
#
# 问题：大量重叠子问题（例如 fib(2) 被重复计算了 3 次）
# 时间复杂度：O(2^n)，非常低效。后续可用记忆化或迭代优化。

def fib(n):
    if n == 0:
        return 0
    if n == 1:
        return 1
    else:
        return fib(n - 2) + fib(n - 1)


