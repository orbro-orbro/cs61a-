# =============================================================================
# 一、List（列表）
# =============================================================================
# List 是 Python 中最常用的序列类型：可变、有序、可装任意类型。

# 1.1 索引（index）—— 从 0 开始的位置偏移量
odds = [41, 43, 47, 49]
print(odds[1])        # → 43（第 2 个元素）

# 1.2 长度
print(len(odds))      # → 4

# 1.3 拼接与重复
digits = [1, 5, 7, 3]
print([2, 7] + digits * 2)   # → [2, 7, 1, 5, 7, 3, 1, 5, 7, 3]
#            ↑ 拼接             ↑ 数字*列表 = 重复

# 1.4 嵌套列表
pairs = [[1, 5], [3, 60], 6]
print(pairs[0])        # → [1, 5]（拿到的是内层列表）
print(pairs[1][1])     # → 60（两级索引，先取内层列表再取元素）


# =============================================================================
# 二、Range（范围）
# =============================================================================
# Range 是不可变的整数序列，不存储所有元素，只存 start/stop/step，按需生成。

# 1 个参数：stop（从 0 开始，不含 stop）
print(list(range(5)))            # → [0, 1, 2, 3, 4]

# 2 个参数：start, stop（含 start，不含 stop）
print(list(range(-3, 5)))        # → [-3, -2, -1, 0, 1, 2, 3, 4]

# Range + for = 最常用的循环组合
for _ in range(5):
    print('hello')               # 打印 5 遍 hello
# _ 是约定俗成的"我不关心这个变量"写法

# Range 不是 List！需要显式转换
# range(5)   ≠   [0, 1, 2, 3, 4]    ← 类型不同
# list(range(5)) 才是 List


# =============================================================================
# 三、for 循环 — 遍历序列
# =============================================================================
# 语法：
#   for <变量名> in <可迭代对象>:
#       <循环体>
#
# 执行过程：① 取序列下一个元素 → ② 绑定到变量 → ③ 执行循环体 → 回到①，直到取完。

def count(s, value):
    """统计 value 在序列 s 中出现的次数"""
    total = 0
    for element in s:             # element 依次绑定 s 的每个元素
        if element == value:
            total += 1
    return total

print(count([1, 2, 1, 3, 1], 1))  # → 3


# =============================================================================
# 四、Unpacking（解包）
# =============================================================================
# for 循环中的元素如果是"固定结构的小容器"（如二元组），可以直接拆开赋值。

pairs = [[1, 2], [2, 2], [3, 2], [4, 4]]
count = 0
for x, y in pairs:                # 将每个 [a, b] 直接拆成 x=a, y=b
    if x == y:
        count += 1
# → count = 2（[2,2] 和 [4,4]）
# 等价于不用解包的写法：
#   for pair in pairs:
#       x, y = pair


# =============================================================================
# 五、Container — in 成员检测
# =============================================================================
# 判断元素是否在容器中，返回布尔值。

print(1 in digits)                 # → True
print(15 in digits)                # → False
# 注意：in 只查"直接元素"，不会递归查嵌套内部
# [[1,5], 6] 中 in 1 → False，因为 1 在外层列表里不是直接元素


# =============================================================================
# 六、List Comprehension（列表推导式）
# =============================================================================
# 用一句话生成一个新列表。语法：
#   [表达式 for 变量 in 序列 if 条件]
#            ↑                         ↑
#        必须要有                     可选

letters = ['a', 'b', 'f', 'p', 'x']

# 根据索引列表提取字母
letter2 = [letters[i] for i in [3, 4, 1]]
print(letter2)                       # → ['p', 'x', 'b']

odds = [1, 3, 5, 7, 9, 15]

# 对每个元素做运算
print([x + 1 for x in odds])         # → [2, 4, 6, 8, 10, 16]

# 加条件过滤
print([x for x in odds if 25 % x == 0])
# → [1, 5]（选出能整除 25 的奇数）

# 推导式 ≈ for 循环的简写：
#   [x+1 for x in odds]  等价于：
#       result = []
#       for x in odds:
#           result.append(x + 1)


# =============================================================================
# 附录：List / Range / Sequence 三者的关系
# =============================================================================
#
# Sequence（序列）是抽象概念——任何支持 索引[s[i]]、len()、for..in 的都属于序列。
#   ├── List（列表）：可变，全量存储，最通用
#   ├── Range（范围）：不可变，只存 start/stop/step，省内存
#   ├── str（字符串）：不可变，字符序列
#   └── tuple（元组）：不可变，常用于函数返回多个值
#
# 一句话：需要改数据 → List；需要循环计数 → Range；统称 Sequence。

# =============================================================================
# 七、Slicing（切片）
# =============================================================================
# 切片 = 一步到位取出一段子序列。语法：
#   seq[start:end]      —— 从 start 到 end-1（含左不含右）
#   seq[start:end:step] —— 还可以指定步长
# 所有序列（List、str、Range、tuple）都支持切片。

odds = [3, 5, 7, 9, 11]

# 基本用法
print(odds[1:3])      # → [5, 7]（索引 1 和 2）

# 省略 start → 从头开始；省略 end → 到末尾
print(odds[:3])       # → [3, 5, 7]
print(odds[2:])       # → [7, 9, 11]

# 负索引 → 从末尾倒数（-1 是最后一个）
print(odds[-2:])      # → [9, 11]（最后两个）

# 步长
print(odds[0:5:2])    # → [3, 7, 11]（每隔一个取一个）
print(odds[::-1])     # → [11, 9, 7, 5, 3]（反转序列！）

# =============================================================================
# 八、Sequence Aggregation（序列聚合）— sum / max / min
# =============================================================================
# 内置的聚合函数，把序列"压缩"成一个值。对 List、Range、tuple 等序列通用。

nums = [3, 1, 7, 2, 9]

# sum(seq)       —— 求和
print(sum(nums))           # → 22

# sum(seq, start) —— 从 start 开始累加（缺省为 0）
print(sum(nums, 100))      # → 122（等价于 100 + 3 + 1 + 7 + 2 + 9）

# max(seq) / min(seq) —— 最大 / 最小值
print(max(nums))           # → 9
print(min(nums))           # → 1

# max(seq, key=...) —— 按自定义规则取最大值
words = ['apple', 'banana', 'kiwi']
print(max(words))                  # → 'kiwi'（按字母序）
print(max(words, key=len))         # → 'banana'（按长度，最长的是 banana）

# all(seq) —— 全真才真；any(seq) —— 有真即真
print(all([1, 2, 3]))             # → True
print(any([0, 0, 1]))             # → True


# =============================================================================
# 九、String（字符串）
# =============================================================================
# 字符串是不可变的字符序列，索引、切片、len、in 全部适用。

s = "Hello, CS61A"

# 索引 & 切片
print(s[0])          # → 'H'
print(s[7:12])       # → 'CS61A'
print(s[::-1])       # → 'A16SC ,olleH'（反转）

# in 成员检测
print('CS' in s)     # → True

# 不可变！下面这行会报错：
# s[0] = 'h'          # TypeError

# 常用方法
print(s.lower())             # → 'hello, cs61a'
print(s.replace('H', 'J'))   # → 'Jello, CS61A'
print(s.split(','))          # → ['Hello', ' CS61A']  拆成列表
print('-'.join(['A','B']))   # → 'A-B'               列表拼成串

# 字符串格式化（f-string）
name = "Xiao"
print(f"Hello, {name}!")     # → 'Hello, Xiao!'


# =============================================================================
# 十、Dictionaries（字典）
# =============================================================================
# 字典是键值对（key-value）容器，通过 key 查 value，而不是通过索引。

d = {'apple': 3, 'banana': 5, 'kiwi': 2}

# 用 key 取值（key 不存在会报错）
print(d['apple'])           # → 3

# 用 .get() 安全取值（key 不存在返回 None 或默认值）
print(d.get('pear', 0))     # → 0

# 增 / 改
d['orange'] = 4              # 新增键值对
d['apple'] = 10              # 修改已有 key 的值

# in 检查 key 是否存在
print('banana' in d)        # → True

# 遍历
for key in d:               # 遍历所有 key
    print(key, d[key])

# .keys() / .values() / .items()
print(list(d))
print(list(d.values()))     # → [10, 5, 2, 4]
for k, v in d.items():      # 同时拿到 key 和 value
    print(f"{k}: {v}")
